const chuvi = (a, b) => (a + b) * 2;
const dientich = (a, b) => a * b;

const a = 3;
const b = 5;

const cv = chuvi(a, b);
const dt = dientich(a, b);

console.log(`Chu vi hinh chu nhat co chieu dai ${a} va chieu rong ${b} la: ${cv}`);
console.log(`Dien tich hinh chu nhat co chieu dai ${a} va chieu rong ${b} la: ${dt}`);